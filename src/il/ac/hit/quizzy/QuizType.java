package il.ac.hit.quizzy;

/**
 * The `QuizType` enum represents the types of quiz interfaces available in the Quizzy application.
 * It defines two constants: TERMINAL and GUI, indicating the terminal-based and graphical user
 * interface (GUI) quiz types, respectively.
 *
 * This enum is used to specify the type of quiz interface when creating a new quiz instance.
 *
 */
public enum QuizType {
    TERMINAL,GUI
}
